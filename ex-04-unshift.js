// Método unshift()

// Arrays em JavaScript podem funcionar tanto como fila quanto como pilha. Eles permitem que você adicione / remova elementos no início ou no final.

// Em informática, a estrutura de dados que permite isso é chamada de deque

let languages = ["JavaScript", "Python"];
console.log(languages);

languages.unshift('Php')// Adiciona 'Php' no início do array
console.log(languages);

languages.unshift('Java')// Adiciona 'Java' no início do array
console.log(languages);

